package caixaeletronico;

public class TesteWhile {
    
    // LOOP WHILE COM VARIAVEL COM CONTADOR:
    
    public void testeLoop() {
        int counter = 0;
        
        while(counter <= 10) {
            System.out.println("CONTADOR " + counter);
            counter++;
        }
        
        
    }
    
    
}
